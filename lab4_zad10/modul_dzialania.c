#include "modul_dzialania.h"
float dodaj(float a,float b)
{
    return a + b;
}
float odejmnij(float a,float b)
{
    return a - b;
}
float pomnoz(float a,float b)
{
    return a * b;
}
float podziel(float a,float b)
{
    return a / b;
}
